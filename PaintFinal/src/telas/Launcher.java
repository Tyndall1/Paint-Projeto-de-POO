    private void telaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_telaMouseClicked
        // Realiza o desenho na tela
        Form desenho;
       
        // Recebe os valores digitados nas caixas
        this.valorLargura = input.inputValor(this, ferramentas, this.inputLargura);
        this.valorAltura = input.inputValor(this, ferramentas, this.inputAltura);
        this.valorDiametro = input.inputValor(this, ferramentas, this.inputDiametro);
        // Desenha ao clicar com o botão direito do mouse
        if(SwingUtilities.isLeftMouseButton(evt)){
            // Irá desenhar um retângulo
            if(desenhar.getControle() == 1){

                this.desenhar.setLargura(this.valorLargura);
                this.desenhar.setAltura(this.valorAltura);
                desenho = desenhar.criarFigura(evt, listaObjetos);
                try{
                    if(this.valorAltura == 0 || this.valorLargura == 0){
                        throw new NumberFormatException();
                    }
                    desenho.desenhar(tela.getGraphics());

                }
                catch(NumberFormatException error){
                     
                    JOptionPane.showMessageDialog(this, "Valor vazio!", "ERROR", JOptionPane.WARNING_MESSAGE);
                }
            }
            
            // Irá desenhar um círculo
            else if(desenhar.getControle() == 2){
                this.desenhar.setDiametro(this.valorDiametro);
                desenho = desenhar.criarFigura(evt, listaObjetos);
                try{
                    if(this.valorDiametro == 0){
                        throw new NumberFormatException();
                    }
                    desenho.desenhar(tela.getGraphics());
                }
                catch (NumberFormatException error){
                    
                    JOptionPane.showMessageDialog(this, "Valor vazio!", "ERROR", JOptionPane.WARNING_MESSAGE);
                }

            }

            // Irá desenhar um triângulo
            else if(desenhar.getControle() == 3){
                this.desenhar.setLargura(this.valorLargura);
                this.desenhar.setAltura(this.valorAltura);
                desenho = desenhar.criarFigura(evt, listaObjetos);
                try{  
                    if(this.valorAltura == 0 || this.valorLargura == 0){
                        throw new NumberFormatException();
                    }
                    desenho.desenhar(tela.getGraphics());              
                }
                catch(NumberFormatException error){
                    
                    JOptionPane.showMessageDialog(this, "Valor vazio!", "ERROR", JOptionPane.WARNING_MESSAGE);
                }
            }
        }
      
       
       
    }//GEN-LAST:event_telaMouseClicked

