    private void ferramentasMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ferramentasMenuActionPerformed
        // Define os valores nas caixas dialogo, e abre a tela
        try{
            this.valorDiametroSub = 0;
            this.valorAlturaSub = 0;
            this.valorLarguraSub = 0;
            int index = listaObjetos.getSelectedIndex();


            if(desenhar.getFiguras().get(listaObjetos.getSelectedIndex()) instanceof Rectangle){
                Rectangle retangulo = (Rectangle)desenhar.getFiguras().get(index);
                String largura = Integer.toString(retangulo.getLargura());
                String altura = Integer.toString(retangulo.getAltura());
                String posX = Integer.toString(retangulo.getX());
                String posY = Integer.toString(retangulo.getY());
                this.posX = retangulo.getX();
                this.posY = retangulo.getY();
                this.valorLarguraSub = retangulo.getLargura();
                this.valorAlturaSub = retangulo.getAltura();
                inputPosXMenu.setText(posX);
                inputPosYMenu.setText(posY);
                inputDiametroMenu.setText("0");
                inputLarguraMenu.setText(largura);
                inputAlturaMenu.setText(altura);
                inputDiametroMenu.setEnabled(false);
                inputAlturaMenu.setEnabled(true);
                inputLarguraMenu.setEnabled(true);
            }

            else if(desenhar.getFiguras().get(listaObjetos.getSelectedIndex()) instanceof Triangle){
                Triangle triangulo = (Triangle)desenhar.getFiguras().get(index);
                String largura = Integer.toString(triangulo.getLargura());
                String altura = Integer.toString(triangulo.getAltura());
                String posX = Integer.toString(retangulo.getX());
                String posY = Integer.toString(retangulo.getY());
                this.posX = triangulo.getX();
                this.posY = triangulo.getY();
                this.valorLarguraSub = triangulo.getLargura();
                this.valorAlturaSub = triangulo.getAltura();
                inputPosXMenu.setText(posX);
                inputPosYMenu.setText(posY);
                inputDiametroMenu.setText("0");
                inputLarguraMenu.setText(largura);
                inputAlturaMenu.setText(altura);
                inputDiametroMenu.setEnabled(false);
                inputAlturaMenu.setEnabled(true);
                inputLarguraMenu.setEnabled(true);
            }

            else if(desenhar.getFiguras().get(listaObjetos.getSelectedIndex()) instanceof Circle){
                Circle circulo = (Circle)desenhar.getFiguras().get(index);
                String diametro = Integer.toString(circulo.getDiametro());
                String posX = Integer.toString(retangulo.getX());
                String posY = Integer.toString(retangulo.getY());
                this.posX = circulo.getX();
                this.posY = circulo.getY();
                this.valorDiametroSub = circulo.getDiametro();
                inputPosXMenu.setText(posX);
                inputPosYMenu.setText(posY);
                inputLarguraMenu.setText("0");
                inputAlturaMenu.setText("0");
                inputDiametroMenu.setText(diametro);
                inputAlturaMenu.setEnabled(false);
                inputLarguraMenu.setEnabled(false);
                inputDiametroMenu.setEnabled(true);
            }

            ferramentas.setLocationRelativeTo(tela);
            ferramentas.setVisible(true);
        }
        catch(ArrayIndexOutOfBoundsException error){
            JOptionPane.showMessageDialog(this, "Selecione uma figura!", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_ferramentasMenuActionPerformed

    private void salvarMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarMenuActionPerformed
        // Modifica os atributos das figuras
        this.valorLarguraSub = input.inputValor(this, ferramentas, inputLarguraMenu);
        this.valorAlturaSub = input.inputValor(this, ferramentas, inputAlturaMenu);
        this.valorDiametroSub = input.inputValor(this, ferramentas, inputDiametroMenu);
        this.posY = input.inputPos(ferramentas, inputPosYMenu);
        this.posX = input.inputPos(ferramentas, inputPosXMenu);
        ferramentas.dispose();
        desenhar.movimentarFigura(tela, tela.getGraphics(), listaObjetos.getSelectedIndex(), this.posX, this.posY);
        desenhar.redimensionarFigura(tela, tela.getGraphics(), listaObjetos.getSelectedIndex(), this.valorLarguraSub, this.valorAlturaSub, this.valorDiametroSub);
        
        
    }//GEN-LAST:event_salvarMenuActionPerformed

